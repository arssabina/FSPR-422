# c копированием

# copy.copy(x) - возвращает поверхностную копию x.

# copy.deepcopy(x) - возвращает полную копию x

a=[2,3,4]
b=a.copy()
b.append(43)
print("a=", a, "b=", b)

# c копированием   (этот вид копирования не копирует строку внутри строки )
a=[2,3,4,[2,3]]
