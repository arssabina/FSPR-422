# Модуль для глубокого копирования (для копирования строки внутри строки) для копирования всех подстрок
import copy


a=[2,3,4,[2,3]]      # исп-ся модуль для глубокого копир-я, потому что в копии когда мы делаем изменения, оригинал не изм.
b=copy.deepcopy(a)
b[2]=400
b [3][1] = 12  
print("a=", a, "b=", b)